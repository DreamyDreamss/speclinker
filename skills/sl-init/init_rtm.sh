#!/bin/bash
# RTM 초기화 — 빈 매트릭스 생성 (bash fallback)

set -e

source project.env

RTM_FILE="docs/02_추적표/RTM_v1.0.md"

if [ -f "$RTM_FILE" ]; then
    echo "RTM 이미 존재: $RTM_FILE (덮어쓰기 생략)"
    exit 0
fi

cat > "$RTM_FILE" << 'RTMEOF'
---
doc_id: RTM-001
doc_type: 요구사항 추적 매트릭스
version: 1.0
status: draft
RTMEOF

cat >> "$RTM_FILE" << EOF
created: $CREATED
updated: $CREATED
project: $PROJECT_NAME
author: Claude (자동 생성)
linked_docs:
  - RD-001
  - SRS-001
---

# 요구사항 추적 매트릭스 (Requirements Traceability Matrix)

> **문서 목적**: FUNC-ID를 공통 키로 설계→코드→테스트 전 체인을 추적한다.
> **DELTA 활용**: \`domain\` 컬럼을 기준으로 sl-change가 관련 ID만 선택적으로 로드한다.

---

## 1. 상태값 범례

| 상태 | 의미 |
|------|------|
| ⬜ 미착수 | 작업 시작 전 |
| 🔄 진행중 | 설계 또는 개발 진행 중 |
| 🧪 테스트중 | 구현 완료, 테스트 진행 중 |
| ✅ 완료 | 테스트 통과, 완전 완료 |
| ❌ 제외 | 범위 제외 또는 취소 |
| 🔁 변경중 | SR에 의해 변경 진행 중 |

---

## 2. 기능 요구사항 추적

| domain | FUNC-ID | 기능명 | SRS-ID | UIS-ID | INF-ID | SCH-ID | TC-ID | SR-ID | 상태 |
|--------|--------|-----------|--------|--------|--------|--------|-------|-------|------|

---

## 3. 비기능 요구사항 추적

| domain | FUNC-ID | 기능명 | SRS-ID | INF-ID | 설계 문서 | 측정 방법 | TC-ID | 상태 |
|--------|--------|-----------|--------|--------|----------|----------|-------|------|

---

## 4. 변경 요구사항 추적 (DELTA)

| SR-ID | FUNC-ID | 변경 유형 | 변경 요약 | domain | 영향 INF | 영향 SCH | 영향 UIS | 변경일 | 상태 |
|-------|--------|---------|----------|--------|---------|---------|---------|--------|------|

---

## 5. 도메인 색인

| domain | API 파일 | DB 파일 | UI 파일 | FUNC 수 |
|--------|---------|---------|---------|--------|

---

## 6. 커버리지 요약

| 구분 | 전체 | 완료 | 진행중 | 미착수 | 완료율 |
|------|------|------|--------|--------|--------|
| 기능 요구사항 | 0 | 0 | 0 | 0 | 0% |
| 비기능 요구사항 | 0 | 0 | 0 | 0 | 0% |
| 변경 요구사항(SR) | 0 | 0 | 0 | 0 | 0% |
| **전체** | **0** | **0** | **0** | **0** | **0%** |

---

## 7. 변경 이력

| 버전 | 날짜 | 변경 내용 | SR-ID | 작성자 |
|------|------|----------|-------|--------|
| 1.0 | $CREATED | 최초 생성 (/sl-init) | | Claude |
EOF

echo "RTM 초기화 완료: $RTM_FILE"
